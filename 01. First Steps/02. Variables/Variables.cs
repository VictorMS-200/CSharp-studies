// Learning variables in C#

namespace Csharp_Studies;

class Variables 
{
    static void Main(string[] args) {

        // Declaring variables
        int age = 18;
        double salario = 5480.50;
        float value = (float) 12.5;
        char letter = (char) 65;
        string name = "Victor";

        Console.WriteLine("My age is equal to: " + age);
        Console.WriteLine("My salary is: " + salario);
        Console.WriteLine("The value is: " + value);
        Console.WriteLine("The value 65 in ASCII is: " + letter);
        Console.WriteLine("My name is: " + name);
    }
}

/*
Output:

My age is equal to: 18
My salary is: 5480.5
The value is: 12.5
The value 65 in ASCII is: A
My name is: Victor
*/

