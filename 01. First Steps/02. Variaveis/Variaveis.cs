using System;

namespace Csharp_Studies {
    class Variaveis {
        static void Main(string[] args) {
            int idade = 18;
            double salario = 5480.50;
            float valor = (float) 12.5;
            char letra = (char) 65;
            string nome = "Victor";

            Console.WriteLine("Minha idade é igual a: " + idade);
            Console.WriteLine("Meu salario é: " + salario);
            Console.WriteLine("O valor é: " + valor);
            Console.WriteLine("O valor 65 em ASCII é: " + letra);
            Console.WriteLine("Meu nome é: " + nome);
        }
    }
}
